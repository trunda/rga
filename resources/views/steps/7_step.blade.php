    <p>
        Rouen [ruán] je severofrancouzské město v Normandii, asi 90 km východně od Le Havre a asi 130 km severozápadně od Paříže. Je hlavním městem regionu Normandie a departementu Seine-Maritime. Městu je někdy přezdíváno Hamburk Západu nebo Gotické Athény. Je nositelem Řádu čestné legie.
    </p>

    <p>
        Kouzlo Jany však nepřestávalo na lid působit, takže se Angličané rozhodli jednou provždy s žijící legendou skoncovat. Zajatou dívku od Burgunďanů koupili za 10 000 liber a odvlekli ji do Rouenu před anglický soud, který měl prokázat její spojení s ďáblem. V procesu, který řídil francouzský biskup Petr Cauchon, byla odsouzena jako čarodějka a kacířka. Dne 30. května 1431 byla devatenáctiletá Jana na rouenském náměstíčku Vieux Marché (Starý trh) upálena. Katolickou církví nebyla exkomunikována a na seznam vyloučených osob z církve byla dána omylem.
    </p>

    <p>
        Když si Karel VII. zajistil vládu nad takřka celou Francií, požádal o obnovení církevního procesu a prohlásil Janu za oběť justičního omylu. Byla rehabilitována světským soudem v plném rozsahu. Později se stala symbolem Katolické ligy. Papež Pius X. ji prohlásil v předválečné době v 18. dubnu 1909 za blahoslavenou a papež Benedikt XV. ji svatořečil 16. května 1920.
    </p>

